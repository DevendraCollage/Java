// todo: Demonstrate the Operator precedence.
public class Pro2 {
    public static void main(String[] args) {
        int first = 10 + 20 * 30;
        System.out.println(first); // Answer : 610
        int second = 100 / 10 * 100;
        System.out.println(second); // Answer : 1000
        int third = 5 * 4 / 4 % 3;
        System.out.println(third); // Answer : 2
        int fourth = 100 + 200 / 10 - 3 * 10;
        System.out.println(fourth); // Answer : 90
    }
}
